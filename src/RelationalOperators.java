
public class RelationalOperators {

	public static void main(String[] args) {

		System.out.println(10<20);
		System.out.println('x'>=20);
		System.out.println('x'<='X');
		System.out.println('X'>20);
		System.out.println(20==20);
		System.out.println(10!=20);
		
		System.out.println((int)'x');
		System.out.println((int)'X');
	}

}
