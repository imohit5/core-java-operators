
public class ArithmeticOperatorsDemo {

	public static void main(String[] args) {
		int a = 50, b = 5;
		
		System.out.println("Add "+(a+b));
		int c = 50;
		int d = ++c;
		System.out.println("Sub "+(a-b));
		
		int e = 50;
		int f = e--;
		System.out.println("Div "+(a/b));
		int g = 50;
		int h = --g;
		System.out.println("Rem "+(a%b));
		
	}
}
